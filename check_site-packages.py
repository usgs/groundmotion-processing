#!/usr/bin/env python
import sys
from pathlib import Path

def main():
    site_packages_path = Path([p for p in sys.path if "site-packages" in p][0])
    metrics_path = site_packages_path / "gmprocess" / "metrics"
    metrics_contents = list(metrics_path.glob("*"))
    for c in metrics_contents:
        print(c)

if __name__ == "__main__":
    main()
